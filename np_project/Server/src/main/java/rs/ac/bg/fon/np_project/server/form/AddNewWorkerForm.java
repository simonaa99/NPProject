    private void validateForm() throws ValidationException{

        Validator.startValidation().validateNotNullOrEmpty(txtIme.getText().trim(), "Morate uneti ime.")
              .validateNotNullOrEmpty(txtPrezime.getText().trim(), "Morate uneti prezime.")
               .validateNotNullOrEmpty(txtUsername.getText().trim(), "Morate uneti korisnicko ime.")
               .validateNotNullOrEmpty(String.valueOf(txtPassword.getPassword()).trim(), "Morate uneti sifru.")
               .validateFormat(String.valueOf(txtPassword.getPassword()).trim(),"........(.)*", "Sifra mora sadrzati najmanje 8 karaktera")
             .validateContainsCharacter(txtUsername.getText(), " ", "Korisnicko ime ne sme sadrzati blanko karakter.")
               .validateContainsCharacter(String.valueOf(txtPassword.getPassword()), " ", "Å ifra ne sme sadrzati blanko karakter.")
              .throwIfInvalide();

    }
