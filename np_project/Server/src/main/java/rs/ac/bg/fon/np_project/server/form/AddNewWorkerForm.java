    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        try {
            validateForm();
            Worker worker=new Worker();
            worker.setFirstName(txtIme.getText().trim());
            worker.setLastName(txtPrezime.getText().trim());
            worker.setUsername(txtUsername.getText().trim());
            worker.setPassword(String.valueOf(txtPassword.getPassword()).trim());
            Controller.getInstance().addWorker(worker);
            JOptionPane.showMessageDialog(this, "Radnik je dodat.");
            this.dispose();
        } catch (ValidationException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Greska", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

