    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        try {

            validateInputs();

            game.setGameName(txtNaz.getText().trim());
            String issueDateStr=txtBrIgraca.getText().trim();
            if(issueDateStr.endsWith(".")){
                issueDateStr=issueDateStr.substring(0,issueDateStr.indexOf("."));
            }
            game.setNumPlayers(Integer.parseInt(issueDateStr));
            game.setGameCategory(GameCategory.valueOf(cmbKategorija.getSelectedItem().toString()));
            game.setNumberInStock(Integer.parseInt(txtKol.getText().trim()));
            Publisher p = new Publisher(null, txtIzdavac.getText().trim());
            game.setPublisher(p);
            ControllerUI.getInstance().addGame(game);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio novu igru!");

        } catch (ValidationException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti igru! ", "Greska", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        removeText();

    }//GEN-LAST:event_btnDodajActionPerformed

