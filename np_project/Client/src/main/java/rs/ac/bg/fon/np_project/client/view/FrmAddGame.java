    private void prepareView() {

        cmbKategorija.removeAllItems();
        
        GameCategory[] categories = GameCategory.values();
        for (GameCategory gameCategory : categories) {
            cmbKategorija.addItem(gameCategory);
        }

    }

    private void validateInputs() throws ValidationException {

        Validator.startValidation().validateNotNullOrEmpty(txtNaz.getText(), "Popunite polje za naziv!").throwIfInvalide();
        Validator.startValidation().validateNotNullOrEmpty(txtBrIgraca.getText(), "Popunite polje za broj igraca!").throwIfInvalide();
        Validator.startValidation().validateNotNullOrEmpty(txtIzdavac.getText(), "Popunite polje za pisca!").throwIfInvalide();
        Validator.startValidation().validateNotNullOrEmpty(txtKol.getText(), "Popunite polje za kolicinu na stanju!").throwIfInvalide();
        Validator.startValidation().validateValueIsNumber(txtKol.getText().trim(), "Kolicina mora biti broj!").throwIfInvalide();
        //Validator.startValidation().validateValueIsNumber(txtBrIgraca.getText().trim(), "Broj igraca mora biti broj!").throwIfInvalide();
        Validator.startValidation().validateNumberNotNegative(txtKol.getText().trim(), "Kolicina ne sme biti negativan broj!").throwIfInvalide();
        Validator.startValidation().validateNumberNotNegative(txtBrIgraca.getText().trim(), "Broj igraca ne sme biti negativan broj!").throwIfInvalide();

    }

    private void removeText() {

        txtNaz.setText("");
        txtBrIgraca.setText("");
        txtKol.setText("");
        txtIzdavac.setText("");

    }
